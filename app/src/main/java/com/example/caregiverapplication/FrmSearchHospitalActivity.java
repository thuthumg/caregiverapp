        caregiverViewModel = ViewModelProviders.of(this).get(CaregiverViewModel.class);

        hospitalListAdapter = new HospitalListAdapter(this);
       // rvNearestHospital.setNestedScrollingEnabled(false);
        rvNearestHospital.setLayoutManager(new LinearLayoutManager(this));
